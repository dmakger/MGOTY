/*
1. Программа начинает с функции `main()`.
2. Пользователю предлагается ввести строку с помощью сообщения "Введите строку: ".
3. Функция `std::getline(std::cin, input)` используется для чтения всей строки, которую ввел пользователь, и сохраняет ее в переменной `input`.
4. Затем вызывается функция `DigitCount(input)`, передавая в нее введенную пользователем строку.
5. Функция `DigitCount()` принимает строку в качестве аргумента `str`.
6. Внутри функции, сначала проверяется базовый случай: если строка `str` пуста (т.е. длина равна 0), то возвращается 0, поскольку в пустой строке нет цифр.
7. Если базовый случай не выполняется, то выполняется рекурсивный случай:
   - Вызывается функция `str.substr(1)`, чтобы получить подстроку `str`, начиная со второго символа (первый символ отбрасывается).
   - Результат вызова `DigitCount(str.substr(1))` прибавляется к 1, чтобы учесть текущий символ в подсчете.
   - Рекурсивный вызов функции продолжается, пока не будет достигнут базовый случай.
   - Когда базовый случай достигнут, каждый рекурсивный вызов возвращает 0 или 1, в зависимости от наличия цифры в текущем символе строки.
   - Результаты рекурсивных вызовов складываются, и в итоге возвращается общее количество цифр в строке.
8. Результат возвращается в функцию `main()`, и сохраняется в переменной `count`.
9. Затем результат выводится на экран с помощью сообщения "Количество цифр в строке: " и значением переменной `count`.
10. Программа завершается с возвратом значения 0 из функции `main()`.

*/

#include <iostream>
#include <string>

// Рекурсивная функция для подсчета количества цифр в строке
int DigitCount(const std::string& str) {
    // Базовый случай: если строка пуста, возвращаем 0
    if (str.empty()) {
        return 0;
    }
    
    // Рекурсивный случай: добавляем 1 к результату и вызываем функцию с оставшейся частью строки
    return 1 + DigitCount(str.substr(1));
}

int main() {
    std::string input;
    
    // Запрос ввода строки у пользователя
    std::cout << "Введите строку: ";
    std::getline(std::cin, input);
    
    // Вызов функции для подсчета количества цифр в строке
    int count = DigitCount(input);
    
    // Вывод результата
    std::cout << "Количество цифр в строке: " << count << std::endl;
    
    return 0;
}
